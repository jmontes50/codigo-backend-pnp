#Operadores Lógicos
#and - y - && => Si las dos condiciones son verdades              todo es verdadero
#or - ó - || => Si alguna de las dos condiciones es verdadera    todo es verdadero
#not - no => Invierte el valor de true o false
#            Si es Verdadero, sera falso y si es Falso sera Verdadero

#and
print((10<10) and (10<20))
#or
print((10<8) or (10<20))
#not
print(not (10<20))